import 'package:flutter/material.dart';

/// Colors used throughout the system.
@immutable
class SystemColors {
  const SystemColors._();

  /// The primary color of the system.
  static const Color primary = Color.fromARGB(255, 0, 0, 0);

  /// The secondary color of the system.
  static const Color secondary = Color.fromARGB(255, 255, 255, 255);

  /// The surface color of the system.
  static const Color surface = Color.fromARGB(255, 0, 0, 0);

  /// The error color of the system.
  static const Color error = Color.fromARGB(255, 255, 0, 0);
}
